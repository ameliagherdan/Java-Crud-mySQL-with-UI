    public void updateData(){
        
        try{
            
            String SQL="update cars set model=?, fuel=?, manmodel=?, year=?, noise=?, pollution=?, price=? where idcars=? ";
                
            int fileSelection=tableCars.getSelectedRow();
            String dao=(String)tableCars.getValueAt(fileSelection, 0);
            PreparedStatement pst=con.prepareStatement(SQL);
            
            pst.setString(1, txtModel.getText());
            
            int selectie=cbFuel.getSelectedIndex();
            pst.setString(2, cbFuel.getItemAt(selectie));
                       
            pst.setString(3, txtMan.getText());
            
            pst.setString(4, numYear.getText());
            
            pst.setString(5, numNoise.getText());
            
            pst.setString(6, numPollution.getText());
            
            pst.setString(7, numPrice.getText());
            
            pst.setString(8, dao);
            pst.execute();
            
            JOptionPane.showMessageDialog(null, "Element updated!");
                   
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: "+e.getMessage());
        }
    }
    
   

