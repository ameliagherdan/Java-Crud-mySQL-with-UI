        public void exportData(String value){
        String[] registered=new String[8];
        String[] number={"ID","Model Car","Fuel Type","Manufacter Name","Year","Noise (db)","Pollution Tax ($)","Price ($)"};
        
       
               
           
            String filename = "d:\\"+value+" engines.txt";
        
        
    
        DefaultTableModel model=new DefaultTableModel(null, number);
        
       String SQL="select * from cars where fuel like '%"+value+"%'";
               
        
        try {
            FileWriter r = new FileWriter(filename);
            Statement st=con.createStatement();
            ResultSet rs2=st.executeQuery(SQL);
            
            while(rs2.next()){
                registered[0]=rs2.getString("idcars");
                r.append(registered[0]+".");
                
                registered[1]=rs2.getString("model");
                r.append(registered[1]+" ");
                
                registered[2]=rs2.getString("fuel");
                r.append(registered[2]+" ");
                
                registered[3]=rs2.getString("manmodel");
                r.append(registered[3]+" ");
                
                registered[4]=rs2.getString("year");
                r.append(registered[4]+" ");
                
                registered[5]=rs2.getString("noise");
                r.append(registered[5]+" ");
                
                registered[6]=rs2.getString("pollution");
                r.append(registered[6]+" ");
                
                registered[7]=rs2.getString("price");
                r.append(registered[7]+" ");
                
                r.append("\n");
                
               
                 
            model.addRow(registered);
                
            }
             JOptionPane.showMessageDialog(null,"Export Successfuly");
            r.close();
            tableCars.setModel(model);
        }   catch (Exception e) {
            JOptionPane.showMessageDialog(null,""+e.getMessage());
        }
        
    }
   
      



